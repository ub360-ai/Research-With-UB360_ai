# Quick Deployment Commands

# Step 1: Add all files to git
git add .

# Step 2: Commit with message
git commit -m "Initial commit: Privacy-first AI Research Assistant with UB360.ai branding"

# Step 3: Create main branch (if needed)
git branch -M main

# Step 4: Add remote (replace YOUR_USERNAME with your GitHub username)
# git remote add origin https://github.com/YOUR_USERNAME/research-assistant-ub360.git

# Step 5: Push to GitHub
# git push -u origin main

# After pushing to GitHub:
# 1. Go to render.com
# 2. Sign in with GitHub
# 3. Click "New +" → "Web Service" for backend
# 4. Click "New +" → "Static Site" for frontend
# 5. Follow RENDER_DEPLOYMENT_GUIDE.md for detailed steps

# Your app will be live at:
# Backend: https://research-assistant-api.onrender.com
# Frontend: https://research-assistant-frontend.onrender.com
